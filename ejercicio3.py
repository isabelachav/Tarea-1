n1=int(input("Ingrese su primer dato \n"))
n2=int(input("Ingrese su segundo dato \n"))
n3=int(input("Ingrese su tercer dato \n"))
n4=int(input("Ingrese su cuarto dato \n"))
n5=int(input("Ingrese su quinto dato \n"))
ressum = n1+n2+n3+n4+n5
resres = n1-n2-n3-n4-n5
resmul = n1*n2*n3*n4*n5
resdiv = n1/n2/n3/n4/n5
print("La suma de los dato es:" + str (ressum))
print("La resta de los dato es:" + str (resres))
print("La multiplicación de los dato es:" + str (resmul))
print("La división de los dato es:" + str (resdiv))